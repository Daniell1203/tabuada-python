n = float(input("Digite um numero: "))

print("=== SUPER TABUADA ===")

print("\n--- MULTIPLICACAO ---")

for i in range(1,11):
    print(f"{n} x {i} = {n*i}")

print("\n--- DIVISAO ---")

for i in range(1,11):
    print(f"{n} / {i} = {n/i}")

print("\n--- SOMA ---")

for i in range(1,11):
    print(f"{n} + {i} = {n+i}")

print("\n--- SUBTRACAO ---")

for i in range(1,11):
    print(f"{n} - {i} = {n-i}")